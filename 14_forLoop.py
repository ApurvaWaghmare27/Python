# For loop with Lists
L1 = [2, 22, 3.14, 37, 1200]
for i in L1:
    print(i)

# For loop with Tuples
T1 = (12, 3, 22.3, False, "Rohan")
for i in T1:
    print(i)

# For loop with Strings
S1 = "Apurva"
for i in S1:
    print(i)

# For loop with else
L2 = [1, 7, 9]
for i in L2:
    print(i)
else:
    print("Done...")

